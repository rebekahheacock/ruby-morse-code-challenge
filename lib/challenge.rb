def decode_morse(morse_code)
  # strip leading/trailing whitespace
  letters = morse_code.strip.gsub("   ", " space ").split(" ")
  message = ''
  letters.each do |i|
    if MORSE_CODE.key?(i)
      message += MORSE_CODE[i]
    elsif i == 'space'
      message += ' '
    else
      message += '*'
    end
  end
  message
end

